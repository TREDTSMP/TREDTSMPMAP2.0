const imagePanel = document.getElementById("imagePanel");
const panelTitle = document.getElementById("panelTitle");
const carouselImage = document.getElementById("carouselImage");
const imageCounter = document.getElementById("imageCounter");

let currentImageIndex = 0;
let imageList = [];

document.getElementById("closePanel").onclick = () => {
  imagePanel.classList.remove("show");
  imagePanel.classList.add("hidden");
};

document.getElementById("prevImage").onclick = () => {
  if (currentImageIndex > 0) {
    currentImageIndex--;
    updateCarousel();
  }
};

document.getElementById("nextImage").onclick = () => {
  if (currentImageIndex < imageList.length - 1) {
    currentImageIndex++;
    updateCarousel();
  }
};

function updateCarousel() {
  carouselImage.src = imageList[currentImageIndex];
  imageCounter.textContent = `${currentImageIndex + 1} / ${imageList.length}`;
}

function openImagePanel(locationName, folderPath) {
  fetch(`/images/${folderPath}/`)
    .then(response => response.text())
    .then(html => {
      // Assuming image file names are predictable or listed
      // Example: image_1.jpg, image_2.jpg, ...
      imageList = [];
      for (let i = 1; i <= 10; i++) {
        const imgPath = `/images/${folderPath}/image_${i}.jpg`;
        imageList.push(imgPath);
      }
      currentImageIndex = 0;
      panelTitle.textContent = locationName;
      updateCarousel();
      imagePanel.classList.remove("hidden");
      imagePanel.classList.add("show");
    });
}
